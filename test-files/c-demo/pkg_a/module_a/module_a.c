#include "module_a.h"

/***********************************************************************************************************************
 * Data
 **********************************************************************************************************************/

static volatile boolean _changeme = FALSE;

/***********************************************************************************************************************
 * Functions
 **********************************************************************************************************************/

void module_a_init (void)
{
    uint32 some_value = 1;

    // nothing to see here
    _changeme = !_changeme;

    CALC (_changeme, some_value);

    if (some_value < MODULE_A_SMTH)
    {
        some_value++;
    }
}
